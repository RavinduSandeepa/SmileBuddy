    private void btnSubmit_SmileActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSubmit_SmileActionPerformed
        String input = InputField_Smile.getText();
        int solution = Integer.parseInt(input);
        boolean correct = myGame.checkSolution(solution);
        score = myGame.getScore();
        if (correct) {
            timer.stop();
            System.out.println("Solution is Correct!");
            currentGame = myGame.nextGame();
            ImageIcon icon = new ImageIcon(currentGame);
            questionField.setIcon(icon);
            score_Smile.setText(String.valueOf(score));
            callTimer();
        } else {
            System.out.println("Incorrect Solution!");
            score_Smile.setText(String.valueOf(score));
            timer.stop();
            noOfAttempts++;
            loadNewGame();
            
        }
    }//GEN-LAST:event_btnSubmit_SmileActionPerformed

