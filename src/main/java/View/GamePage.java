    private void callTimer() {
        timerSmile.setText("01:00");
        second = 0;
        minute = 1;
        countdownTimer();
        timer.start();
    }
    
    public void loadNewGame(){
        attemptsField.setText(noOfAttempts+"/5");
        if (noOfAttempts == 6) {
            try {
                scoresData.insertScore(newUser.getUsername(), score, game);
            } catch (SQLException ex) {
                System.out.println("Inserting unsuccessful when attempts exceeded!");
            }
            dispose();
            MainMenu mainMenu = new MainMenu(newUser);
            mainMenu.setVisible(true);
        }
        else{
            currentGame = myGame.nextGame();
            ImageIcon nxtIcon = new ImageIcon(currentGame);
            questionField.setIcon(nxtIcon);
            callTimer();
            
        }
        
    }
